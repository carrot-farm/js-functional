/** =======================================
 *  range, map, filter, take의 중첩 사용
 ======================================= */
go(range(10),
  map(n => n + 10),
  filter(n => n % 2),
  take(2),
  log); // [11, 13]

go(L.range(10),
  L.map(n => n + 10),
  L.filter(n => n % 2),
  take(2),
  log); // [11, 13]


